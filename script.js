function mouseOver(element) {
    // element.style.background='linear-gradient(to right, red, lightblue)';
    element.innerHTML = 'Hello PANGET'
    element.style.fontSize = '3rem'
}
function mouseOut(element) {
    // element.style.background='linear-gradient(to right, blue, lightblue)';
    element.innerHTML = 'Hello WORLD'
    element.style.fontSize = '2rem'
}
